uint8_t rgb2vga(int r, int g, int b) {
  float rf = (float)r, gf = (float)g, bf = (float)b;
  float closest = +INFINITY;
  int ndx = 0, done = 0;
  // This is slow. For some reasons, those pragmas made it extra slow.
  //  #pragma omp parallel for
  for (int i = 0; i < 248; i++) {
    //    if (done) continue;
    float *sample = ndx_vgapal[i];
    float rs = sample[0];
    float gs = sample[1];
    float bs = sample[2];
    float dst =
        sqrt(powf(rs - rf, 2.0) + powf(gs - gf, 2.0) + powf(bs - bf, 2.0));
    //   #pragma omp critical
    {
      if (closest > dst) {
        closest = dst;
        ndx = i;
      } else if (dst < 0.05) {
        done = 1;
        ndx = i;
        break;
      }
    }
  }
  return (uint8_t)ndx;
}
